        <h1>Welcome to the University of Edinburgh L&amp;UC Projects Index</h1>

        <p>Access to this index is restricted to specific members of staff.</p>

        <p>Library & University Collections Projects index serves as a central point for retention and sharing of current
            and historical L&UC externally funded project information and project documentation.  The site will provide
            an overview of all completed, current and pending projects. Access to information will be restricted to relevant
            staff members.  The site will be managed by the Projects and Innovation Team, however, it will be the
            responsibility of individual project staff to ensure that the information relevant to their particular
            projects are kept up to date.  The information provided can be used for on-going project management,
            future project planning, in the preparation of new funding bids and more.
        </p>
        <p>Projects can be searched using the search box above and/or browsed using the options to the right.  There are
            a number of ways to search the site including by funder, project, subject and Principle Investigator.  There
            is also an advanced search option as above.
        </p>
