        <p>
            <strong>Making a complaint</strong></p>

        <p>In the body of the email please provide the following information:</p>
        <p>
        <ul>
            <li>Describe the infringement in as much detail as possible so that the specific content may be readily
                identified. The URL in the address bar will allow us to lookup the specific section.
            </li>
            <li>Describe your relationship to the content, e.g. I am the author/creator of the material.</li>
            <li>Describe the grounds for complaint, some examples are:
                <ul>
                    <li>Unauthorised use by reason of reproduction and/or making available the material.</li>
                    <li>Breach of the moral right of [paternity/integrity/right not to have my work subjected to
                        derogatory treatment].
                    </li>
                    <li>Other complaints, e.g. defamation, breach of confidence, data protection.</li>
                </ul>
            </li>
        </ul>
        <strong>On receipt of your complaint, the Director of the Institute of Governance will:</strong><br/>
        <ol>
            <li>Make an initial assessment of its validity</li>
            <li>For all but spurious complaints, temporarily remove access to the item that is subject to complaint</li>
            <li>Acknowledge receipt of the complaint by email</li>
            <li>Seek to verify your identity and authority as complainant</li>
            <li>Refer the complaint to the University's Legal Advisor for comment and advice</li>
        </ol></p>
        <p><strong>Potential outcomes:</strong></p>

        <p>When the Director of the Institute of Governance has verified the authenticity of your complaint and has been advised
            that it is ostensibly legitimate, the file will be permanently removed from public access.<br/>
            <strong>OR</strong><br/>
            If the Legal Advisor confirms that it does not breach any law then the item will be reinstated.</p>
