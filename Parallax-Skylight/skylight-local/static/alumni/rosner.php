        <h1>Students of Medicine, 1762-1826</h1>
        <h2><a href='./search/*:*/Collection:"students+of+medicine%2C+1762-1826%7C%7C%7CStudents+of+Medicine%2C+1762-1826"'><i class="fa fa-search fa-lg">&nbsp;</i>View all items</a></h2>
        <p>The foundation of this database lies in an earlier database compiled by Dr. Lisa M. Rosner, It was was compiled from the following:</p>
        <ul>
            <li>University of Edinburgh: Matriculation Albums: 1762-1786, 1786-1805, 1804-1816 (3 vols.). [Edinburgh University Library Special Collections: EUA IN1/ADS/STA/2]</li>
            <li>Comrie, J.D. and Gardner, J.J, Biographical Index of Edinburgh Medical Graduates, 1705-1866 [Edinburgh University Library Special Collections]</li>
            <li><u>List of the Graduates in Medicine in the University of Edinburgh from MDCCV to MDCCCLXVI</u> (Edinburgh, Neil &amp; Co., 1867)</li>
            <li><u>List of the Members, Laws, and Library-Catalogue of the Medical Society of Edinburgh, instituted 1737; incorporated by Royal Charter Dec. 14, 1778</u> (Edinburgh, Printed for the Society by William Aitken, 1820)</li>
            <li>Lists of apprentices, diplomates (later called Licentiates), and members (later called Fellows) of the Royal College of Surgeons of Edinburgh (taken from Minute Books, vols. 4-8, 1708-1822)</li>
            <li>Crawford, Dirom Grey, <u>Roll of the Indian Medical Service</u> (London, W. Thacker &amp; Co., 1930)</li>
            <li>Johnston, William, <u>Roll of Commissioned Officers in the Medical Service of the British Army</u> (Aberdeen, Aberdeen University Press, 1917)</li>
            <li>Turnbull, William, <u>The Naval Surgeon: Comprising the Entire Duties of Professional Men at Sea</u> (London, Richard Phillips, 1806)</li>
        </ul>

        <p>This data was used in the compilation of the following publication by Dr. Rosner:</p>

        <ul>
            <li><u>Medical education in the Age of Improvement : Edinburgh students and apprentices, 1760-1826</u> (Edinburgh, Edinburgh University Press, 1991)</li>
        </ul>

        <p>This original data has been augmented by links to digital images taken from the earliest University of Edinburgh: Laureation &amp; Degrees Album.</p>

        <p><b>Please note: </b> There is no guarantee that occurences of the same name always relate to the same person (e.g. John Robertson studying in 1798 might not be the same John Robertson listed for 1799).  The composite record which has been created in this database offers the best analysis of the data available but should not be considered definitive.</p>


